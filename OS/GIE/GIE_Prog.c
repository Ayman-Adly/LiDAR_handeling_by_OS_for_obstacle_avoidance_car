#include "GIE_Interface.h"


/**************************************************************************************************************
 * @brief enable general interrupt
 * 
 *************************************************************************************************************/
void GIE_vidEnable(void)
{
//set the bit
SREG_REG |= (1<<7);

}

/**************************************************************************************************************
 * @brief disable general interrupt
 * 
 *************************************************************************************************************/
void GIE_vidDisable(void)
{
//clear the bit
SREG_REG &= ( ~(1<<7) );

}
